# Load .env file before anything else
try:
    from dotenv import load_dotenv

    load_dotenv(Path(__file__).parent.parent / ".env")
    print("📝 Loaded .env file")
except ImportError:
    # If python-dotenv not installed, try manual loading
    env_path = Path(__file__).parent.parent / ".env"
    if env_path.exists():
        with open(env_path) as f:
            for line in f:
                line = line.strip()
                if line and not line.startswith("#") and "=" in line:
                    key, value = line.split("=", 1)
                    os.environ.setdefault(key.strip(), value.strip().strip('"').strip("'"))
        print("📝 Loaded .env file (manual)")


@dataclass
class BenchmarkSlot:
    """A single benchmark experiment slot."""

    name: str
    mode: str  # individual | team
    symbol: str = "BTCUSDT"  # Symbol to trade
    base_model: str | None = None
    team_models: str | None = None
    experiment: str = ""
    experiment_id: int = 0
    slot_index: int = 0
    description: str = ""
    monolithic_mode: bool = False
    disable_reasoning_bank: bool = False
    disable_risk_manager: bool = False
    disable_judge: bool = False
    single_timeframe: str | None = None
    bias_tf: str | None = None
    entry_tf: str | None = None
    scout_tf: str | None = None


def _slug(value: str) -> str:
    return re.sub(r"[^a-zA-Z0-9._-]+", "_", value)


def _append_jsonl(path: Path, payload: dict) -> None:
    with path.open("a") as f:
        f.write(json.dumps(payload, default=str) + "\n")


def _load_plan(path: Path) -> list[BenchmarkSlot]:
    """Load experiment plan from JSON file."""
    raw = json.loads(path.read_text())
    slots: list[BenchmarkSlot] = []
    for i, item in enumerate(raw, start=1):
        if not isinstance(item, dict):
            raise ValueError(f"Invalid plan item at index {i}: expected dict")
        mode = str(item.get("mode", "individual")).strip().lower()
        name = str(item.get("name", f"slot-{i}")).strip()
        if mode not in {"individual", "team"}:
            raise ValueError(f"Invalid mode at slot {i}: {mode}")
        if mode == "individual" and not item.get("base_model"):
            raise ValueError(f"Slot {i} requires base_model in individual mode")
        if mode == "team" and not item.get("team_models"):
            raise ValueError(f"Slot {i} requires team_models in team mode")

        slot = BenchmarkSlot(
            name=name,
            mode=mode,
            symbol=item.get("symbol", "BTCUSDT"),
            base_model=item.get("base_model"),
            team_models=item.get("team_models"),
            experiment=item.get("experiment", ""),
            experiment_id=item.get("experiment_id", 0),
            slot_index=item.get("slot_index", i),
            description=item.get("description", ""),
            monolithic_mode=item.get("monolithic_mode", False),
            disable_reasoning_bank=item.get("disable_reasoning_bank", False),
            disable_risk_manager=item.get("disable_risk_manager", False),
            disable_judge=item.get("disable_judge", False),
            single_timeframe=item.get("single_timeframe"),
            bias_tf=item.get("bias_tf"),
            entry_tf=item.get("entry_tf"),
            scout_tf=item.get("scout_tf"),
        )
        slots.append(slot)
    return slots


def _build_cmd(
    slot: BenchmarkSlot,
    args: argparse.Namespace,
    *,
    run_tag: str,
    slot_number: int,
) -> list[str]:
    """Build the subprocess command for a slot."""
    bias_tf = slot.bias_tf or args.bias_tf
    entry_tf = slot.entry_tf or args.entry_tf
    scout_tf = slot.scout_tf or args.scout_tf

    if slot.single_timeframe:
        tf = slot.single_timeframe
        bias_tf = tf
        entry_tf = tf
        scout_tf = tf

    # Use slot-specific symbol if available, otherwise fall back to args.symbol
    symbol = slot.symbol if hasattr(slot, "symbol") and slot.symbol else args.symbol

    cmd = [
        args.python_bin,
        "scripts/run_hybrid_live_paper.py",
        "--symbol",
        symbol,
        "--timeframes",
        args.timeframes,
        "--bias-tf",
        bias_tf,
        "--entry-tf",
        entry_tf,
        "--scout-tf",
        scout_tf,
        "--run-minutes",
        str(args.slot_minutes),
        "--position-usd",
        str(args.position_usd),
        "--run-tag",
        run_tag,
        "--slot-name",
        slot.name,
        "--slot-index",
        str(slot_number),
        "--experiment",
        slot.experiment or "",
        "--experiment-id",
        str(slot.experiment_id),
    ]

    if slot.mode == "individual" and slot.base_model:
        cmd.extend(
            [
                "--base-model",
                str(slot.base_model),
                "--base-vision-model",
                args.vision_model,
            ]
        )
    elif slot.mode == "team" and slot.team_models:
        cmd.extend(
            [
                "--team-models",
                str(slot.team_models),
                "--team-tag",
                slot.name,
            ]
        )
    elif slot.base_model:
        cmd.extend(
            [
                "--base-model",
                str(slot.base_model),
                "--base-vision-model",
                args.vision_model,
            ]
        )

    if slot.monolithic_mode:
        cmd.append("--monolithic-mode")

    if slot.disable_reasoning_bank:
        cmd.append("--disable-reasoning-bank")

    if slot.disable_risk_manager:
        cmd.append("--disable-risk-manager")

    if slot.disable_judge:
        cmd.append("--disable-judge")

    if slot.single_timeframe:
        cmd.extend(["--single-timeframe", slot.single_timeframe])

    return cmd


def _run_auto_analysis(
    schedule_path: Path,
    run_tag: str,
    python_bin: str,
    env: dict[str, str],
    log_dir: Path,
    symbol: str,
) -> dict | None:
    """Run analyze_benchmark_results.py and capture summary."""
    analysis_script = Path("scripts/analyze_benchmark_results.py")
    if not analysis_script.exists():
        return None

    report_path = log_dir / f"benchmark_report_{run_tag}.json"
    try:
        result = subprocess.run(
            [
                python_bin,
                str(analysis_script),
                "--schedule-log",
                str(schedule_path),
                "--signal-dir",
                str(log_dir),
                "--symbol",
                symbol,
                "--min-n",
                "5",
                "--output",
                str(report_path),
            ],
            env=env,
            capture_output=True,
            text=True,
            timeout=180,
        )
        if result.returncode == 0:
            payload: dict[str, Any] = {"status": "analysis_completed"}
            if report_path.exists():
                try:
                    parsed = json.loads(report_path.read_text())
                    payload["report_path"] = str(report_path)
                    payload["summary"] = parsed.get("summary", {})
                    payload["slot_metrics"] = parsed.get("slot_metrics", [])
                except Exception:
                    payload["report_path"] = str(report_path)
            return payload
        else:
            return {
                "status": "analysis_failed",
                "returncode": result.returncode,
                "stderr": result.stderr[:500] if result.stderr else "",
            }
    except subprocess.TimeoutExpired:
        return {"status": "analysis_timeout"}
    except Exception as e:
        return {"status": "analysis_error", "error": str(e)}


def _check_alerts(
    slot: BenchmarkSlot,
    analysis_result: dict | None,
    master_log,
) -> None:
    """Print alerts if a slot shows concerning metrics."""
    if not analysis_result or analysis_result.get("status") != "analysis_completed":
        return

    slot_metrics = analysis_result.get("slot_metrics", [])
    current = None
    for item in slot_metrics:
        if str(item.get("slot_name")) == slot.name:
            current = item
    if not current:
        return

    alerts = []
    directional_n = int(current.get("directional_samples", 0) or 0)
    accuracy = current.get("directional_acc_pct")
    timeout_rate = current.get("timeout_rate_pct")
    risk_unknown_rate = current.get("risk_invalid_rate_pct")

    if directional_n >= 8 and accuracy is not None and float(accuracy) < 30.0:
        alerts.append(f"⚠️  LOW ACCURACY: {slot.name} → {float(accuracy):.1f}% (n={directional_n})")
    if timeout_rate is not None and float(timeout_rate) > 40.0:
        alerts.append(f"⚠️  HIGH TIMEOUT: {slot.name} → {float(timeout_rate):.1f}%")
    if risk_unknown_rate is not None and float(risk_unknown_rate) > 50.0:
        alerts.append(f"⚠️  HIGH RISK-UNKNOWN: {slot.name} → {float(risk_unknown_rate):.1f}%")

    for alert in alerts:
        print(alert)
        master_log.write(f"  {alert}\n")


def main() -> None:
    parser = argparse.ArgumentParser(
        description="FenixAI Benchmark Suite — run controlled LLM experiments"
    )
    parser.add_argument("--plan-json", required=True, help="JSON plan file path")
    parser.add_argument("--symbol", default="BTCUSDT")
    parser.add_argument("--timeframes", default="3m,5m,15m,30m,1h")
    parser.add_argument("--bias-tf", default="1h")
    parser.add_argument("--entry-tf", default="5m")
    parser.add_argument("--scout-tf", default="3m")
    parser.add_argument("--slot-minutes", type=int, default=60, help="Minutes per slot")
    parser.add_argument("--position-usd", type=float, default=1000.0)
    parser.add_argument("--vision-model", default="qwen3-vl:235b-cloud")
    parser.add_argument("--python-bin", default="fenix_env/bin/python")
    parser.add_argument(
        "--max-slots", type=int, default=None, help="Limit number of slots (for testing)"
    )
    parser.add_argument("--resume-from", type=int, default=1, help="Resume from slot N (1-indexed)")
    parser.add_argument(
        "--auto-analyze",
        action="store_true",
        default=True,
        help="Run analysis after each slot (default: true)",
    )
    parser.add_argument("--no-auto-analyze", action="store_false", dest="auto_analyze")
    parser.add_argument(
        "--api-key-index",
        type=int,
        default=1,
        choices=[1, 2],
        help="API key index to use (1 or 2) for Ollama Cloud and Binance Testnet",
    )
    parser.add_argument(
        "--run-tag-suffix",
        type=str,
        default="",
        help="Optional suffix for run tag (e.g., '_key1', '_key2')",
    )
    parser.add_argument(
        "--tech-timeout-short-sec",
        type=float,
        default=65.0,
        help="Short-TF timeout (seconds) for Technical agent",
    )
    parser.add_argument(
        "--qabba-timeout-short-sec",
        type=float,
        default=65.0,
        help="Short-TF timeout (seconds) for QABBA agent",
    )
    parser.add_argument(
        "--decision-timeout-short-sec",
        type=float,
        default=45.0,
        help="Short-TF timeout (seconds) for Decision agent",
    )
    parser.add_argument(
        "--visual-timeout-short-sec",
        type=float,
        default=20.0,
        help="Short-TF timeout (seconds) for Visual agent",
    )
    parser.add_argument(
        "--tech-max-retries", type=int, default=1, help="Max retries for Technical agent"
    )
    parser.add_argument(
        "--qabba-max-retries", type=int, default=1, help="Max retries for QABBA agent"
    )
    parser.add_argument(
        "--decision-max-retries", type=int, default=1, help="Max retries for Decision agent"
    )
    parser.add_argument(
        "--sentiment-max-retries", type=int, default=1, help="Max retries for Sentiment agent"
    )
    parser.add_argument(
        "--sentiment-agent-timeout-short-sec",
        type=float,
        default=22.0,
        help="Short-TF timeout (seconds) for Sentiment agent",
    )
    parser.add_argument(
        "--sentiment-agent-timeout-sec",
        type=float,
        default=32.0,
        help="Default timeout (seconds) for Sentiment agent",
    )
    parser.add_argument(
        "--retry-429-wait-sec",
        type=float,
        default=8.0,
        help="Base wait in seconds before retrying 429/concurrency errors",
    )
    parser.add_argument(
        "--retry-429-wait-jitter-sec",
        type=float,
        default=2.0,
        help="Random jitter in seconds added to --retry-429-wait-sec",
    )
    parser.add_argument(
        "--llm-max-concurrent-requests",
        type=int,
        default=8,
        help="Global per-process cap for concurrent LLM requests (0 disables limiter)",
    )
    parser.add_argument(
        "--max-tokens-multiplier",
        type=float,
        default=1.0,
        help="Multiplier for agent max_tokens in run_hybrid_live_paper (e.g., 2.0 or 3.0)",
    )
    args = parser.parse_args()

    # Load plan
    plan_path = Path(args.plan_json)
    if not plan_path.exists():
        print(f"❌ Plan file not found: {plan_path}")
        sys.exit(1)

    slots = _load_plan(plan_path)
    if not slots:
        print("❌ Empty plan file")
        sys.exit(1)

    # Apply limits
    if args.max_slots:
        slots = slots[: args.max_slots]

    # Setup logging
    log_dir = Path("logs")
    log_dir.mkdir(parents=True, exist_ok=True)
    run_tag = datetime.now(timezone.utc).strftime("%Y%m%d_%H%M%S")

    python_bin = args.python_bin
    if not Path(python_bin).exists():
        print(f"❌ Python binary not found: {python_bin}")
        sys.exit(1)

    # Environment setup
    env = os.environ.copy()
    env["PYTHONPATH"] = "."
    env["LLM_PROFILE"] = "OLLAMA_CLOUD"
    env["OLLAMA_CLOUD_URL"] = env.get("OLLAMA_CLOUD_URL", "https://api.ollama.com")
    env["DISABLE_MLX"] = env.get("DISABLE_MLX", "1")
    env["PYTHONUNBUFFERED"] = "1"  # Force unbuffered output so slot logs flush in real time
    env["MPLCONFIGDIR"] = env.get("MPLCONFIGDIR", "/tmp/matplotlib")
    env["XDG_CACHE_HOME"] = env.get("XDG_CACHE_HOME", "/tmp")

    # Multi-API key support: select Ollama Cloud and Binance Testnet keys by index
    api_key_idx = args.api_key_index
    ollama_key = env.get(f"OLLAMA_CLOUD_API_KEY_{api_key_idx}")
    if ollama_key:
        env["OLLAMA_CLOUD_API_KEY"] = ollama_key
        # Some clients still expect OLLAMA_API_KEY.
        env["OLLAMA_API_KEY"] = ollama_key
        key_fingerprint = f"***{ollama_key[-6:]}" if len(ollama_key) >= 6 else "***"
        print(f"🔑 Using OLLAMA_CLOUD_API_KEY_{api_key_idx} ({key_fingerprint})")
    else:
        print(
            f"⚠️ OLLAMA_CLOUD_API_KEY_{api_key_idx} not found, falling back to OLLAMA_CLOUD_API_KEY"
        )
        if env.get("OLLAMA_CLOUD_API_KEY"):
            env.setdefault("OLLAMA_API_KEY", env["OLLAMA_CLOUD_API_KEY"])

    if "localhost" in str(env.get("OLLAMA_CLOUD_URL", "")).lower():
        print(
            "⚠️ OLLAMA_CLOUD_URL points to localhost; cloud key isolation may not apply as expected"
        )

    binance_key = env.get(f"BINANCE_TESTNET_API_KEY_{api_key_idx}")
    binance_secret = env.get(f"BINANCE_TESTNET_API_SECRET_{api_key_idx}")
    if binance_key and binance_secret:
        env["BINANCE_TESTNET_API_KEY"] = binance_key
        env["BINANCE_TESTNET_API_SECRET"] = binance_secret
        print(f"🔑 Using BINANCE_TESTNET_API_KEY_{api_key_idx}")
    else:
        print(f"⚠️ BINANCE_TESTNET_API_KEY_{api_key_idx} not found, falling back to default")

    # Run tag suffix for parallel runs
    if args.run_tag_suffix:
        run_tag = f"{run_tag}{args.run_tag_suffix}"

    # Use final run_tag everywhere to keep logs/report names aligned.
    schedule_path = log_dir / f"benchmark_schedule_{run_tag}.jsonl"
    results_path = log_dir / f"benchmark_results_{run_tag}.jsonl"
    master_log_path = log_dir / f"benchmark_run_{run_tag}.log"

    # Short-TF hardening
    env.setdefault("FENIX_SHORT_TF_NONBLOCKING", "1")
    env.setdefault("FENIX_TECH_TIMEOUT_SHORT_SEC", str(args.tech_timeout_short_sec))
    # Backward-compatible alias used in older docs/scripts.
    env.setdefault("FENIX_TECHNICAL_TIMEOUT_SHORT_SEC", env["FENIX_TECH_TIMEOUT_SHORT_SEC"])
    env.setdefault("FENIX_QABBA_TIMEOUT_SHORT_SEC", str(args.qabba_timeout_short_sec))
    env.setdefault("FENIX_DECISION_TIMEOUT_SHORT_SEC", str(args.decision_timeout_short_sec))
    env.setdefault("FENIX_VISUAL_TIMEOUT_SHORT_SEC", str(args.visual_timeout_short_sec))
    env.setdefault("FENIX_TECH_MAX_RETRIES", str(args.tech_max_retries))
    env.setdefault("FENIX_TECHNICAL_MAX_RETRIES", env["FENIX_TECH_MAX_RETRIES"])
    env.setdefault("FENIX_QABBA_MAX_RETRIES", str(args.qabba_max_retries))
    env.setdefault("FENIX_DECISION_MAX_RETRIES", str(args.decision_max_retries))
    env.setdefault("FENIX_SENTIMENT_MAX_RETRIES", str(args.sentiment_max_retries))
    env.setdefault(
        "FENIX_SENTIMENT_AGENT_TIMEOUT_SHORT_SEC", str(args.sentiment_agent_timeout_short_sec)
    )
    env.setdefault("FENIX_SENTIMENT_AGENT_TIMEOUT_SEC", str(args.sentiment_agent_timeout_sec))
    env.setdefault("FENIX_RETRY_429_WAIT_SEC", str(args.retry_429_wait_sec))
    env.setdefault("FENIX_RETRY_429_WAIT_JITTER_SEC", str(args.retry_429_wait_jitter_sec))
    env.setdefault("FENIX_LLM_MAX_CONCURRENT_REQUESTS", str(args.llm_max_concurrent_requests))
    env["FENIX_MAX_TOKENS_MULTIPLIER"] = str(args.max_tokens_multiplier)
    env.setdefault("FENIX_DISABLE_VISUAL_SHORT_TF", "1")
    env.setdefault("FENIX_DISABLE_SENTIMENT_SHORT_TF", "1")

    # Save run metadata
    run_metadata = {
        "event": "run_start",
        "run_tag": run_tag,
        "plan_file": str(plan_path),
        "total_slots": len(slots),
        "slot_minutes": args.slot_minutes,
        "symbol": args.symbol,
        "timeframes": args.timeframes,
        "bias_tf": args.bias_tf,
        "entry_tf": args.entry_tf,
        "scout_tf": args.scout_tf,
        "vision_model": args.vision_model,
        "resume_from": args.resume_from,
        "timestamp": datetime.now(timezone.utc).isoformat(),
        "experiments": sorted({s.experiment for s in slots if s.experiment}),
    }
    _append_jsonl(schedule_path, run_metadata)
    _append_jsonl(results_path, run_metadata)

    total = len(slots)
    completed = 0
    failed = 0

    print(f"\n{'=' * 70}")
    print("  FenixAI Benchmark Suite v1.0")
    print(f"  Plan: {plan_path.name}")
    print(f"  Slots: {total} × {args.slot_minutes}min = ~{total * args.slot_minutes / 60:.1f}h")
    print(f"  Experiments: {', '.join(run_metadata['experiments']) or 'n/a'}")
    print(f"  Run tag: {run_tag}")
    print(f"{'=' * 70}\n")

    with master_log_path.open("a") as master:
        master.write(
            f"[{datetime.now(timezone.utc).isoformat()}] START run_tag={run_tag} "
            f"slots={total} slot_minutes={args.slot_minutes}\n"
        )
        master.write(f"  plan={plan_path} experiments={run_metadata['experiments']}\n")
        master.flush()

        for idx, slot in enumerate(slots, start=1):
            if idx < args.resume_from:
                print(
                    f"  ⏭  Slot {idx}/{total}: {slot.name} (skipped — resume_from={args.resume_from})"
                )
                continue

            slot_start = datetime.now(timezone.utc)
            slot_log = log_dir / f"bench_slot_{idx:02d}_{_slug(slot.name)}_{run_tag}.log"

            # Build command
            cmd = _build_cmd(slot, args, run_tag=run_tag, slot_number=idx)

            # Log slot start
            slot_event = {
                "event": "slot_start",
                "slot": idx,
                "slot_name": slot.name,
                "mode": slot.mode,
                "experiment": slot.experiment,
                "experiment_id": slot.experiment_id,
                "base_model": slot.base_model,
                "team_models": slot.team_models,
                "monolithic_mode": slot.monolithic_mode,
                "disable_reasoning_bank": slot.disable_reasoning_bank,
                "single_timeframe": slot.single_timeframe,
                "bias_tf": slot.bias_tf,
                "entry_tf": slot.entry_tf,
                "scout_tf": slot.scout_tf,
                "description": slot.description,
                "timestamp": slot_start.isoformat(),
                "command": cmd,
                "slot_log": str(slot_log),
            }
            _append_jsonl(schedule_path, slot_event)
            _append_jsonl(results_path, slot_event)

            flags = []
            if slot.monolithic_mode:
                flags.append("MONO")
            if slot.disable_reasoning_bank:
                flags.append("NO-RB")
            if slot.single_timeframe:
                flags.append(f"TF={slot.single_timeframe}")
            flag_str = f" [{','.join(flags)}]" if flags else ""

            print(f"\n  🔬 Slot {idx}/{total}: {slot.name}{flag_str}")
            print(f"     {slot.description}")
            print(f"     Model: {slot.base_model or slot.team_models or 'default'}")

            master.write(
                f"[{slot_start.isoformat()}] SLOT {idx}/{total} name={slot.name} "
                f"exp={slot.experiment} mode={slot.mode}{flag_str}\n"
            )
            master.flush()

            # Run slot
            rc = None
            timeout_sec = max(120, args.slot_minutes * 60 + 900)
            with slot_log.open("w") as out:
                try:
                    result = subprocess.run(
                        cmd,
                        env=env,
                        stdout=out,
                        stderr=subprocess.STDOUT,
                        timeout=timeout_sec,
                        check=False,
                    )
                    rc = int(result.returncode)
                except subprocess.TimeoutExpired:
                    rc = -9
                    out.write("\n[BENCHMARK] Slot timeout exceeded; moving to next slot.\n")

            slot_end = datetime.now(timezone.utc)
            duration_sec = (slot_end - slot_start).total_seconds()

            status = "✅" if rc == 0 else ("⏱ TIMEOUT" if rc == -9 else f"❌ rc={rc}")
            print(f"     {status} ({duration_sec:.0f}s)")

            # Log slot end
            end_event = {
                "event": "slot_end",
                "slot": idx,
                "slot_name": slot.name,
                "mode": slot.mode,
                "experiment": slot.experiment,
                "base_model": slot.base_model,
                "team_models": slot.team_models,
                "monolithic_mode": slot.monolithic_mode,
                "disable_reasoning_bank": slot.disable_reasoning_bank,
                "single_timeframe": slot.single_timeframe,
                "timestamp": slot_end.isoformat(),
                "duration_sec": duration_sec,
                "return_code": rc,
                "slot_log": str(slot_log),
            }
            _append_jsonl(schedule_path, end_event)
            _append_jsonl(results_path, end_event)

            master.write(
                f"[{slot_end.isoformat()}] SLOT {idx} name={slot.name} "
                f"rc={rc} duration={duration_sec:.0f}s\n"
            )
            master.flush()

            if rc == 0:
                completed += 1
            else:
                failed += 1

            # Auto-analysis
            if args.auto_analyze and rc == 0:
                print("     📊 Running auto-analysis...")
                analysis = _run_auto_analysis(
                    schedule_path, run_tag, python_bin, env, log_dir, args.symbol
                )
                if analysis:
                    analysis_event = {
                        "event": "auto_analysis",
                        "slot": idx,
                        "slot_name": slot.name,
                        "experiment": slot.experiment,
                        "timestamp": datetime.now(timezone.utc).isoformat(),
                        "analysis": analysis,
                    }
                    _append_jsonl(results_path, analysis_event)
                    _check_alerts(slot, analysis, master)

        # Summary
        run_end = datetime.now(timezone.utc)
        summary = {
            "event": "run_end",
            "run_tag": run_tag,
            "total_slots": total,
            "completed": completed,
            "failed": failed,
            "skipped": max(0, args.resume_from - 1),
            "timestamp": run_end.isoformat(),
        }
        _append_jsonl(results_path, summary)

        master.write(
            f"\n[{run_end.isoformat()}] END run_tag={run_tag} "
            f"completed={completed} failed={failed}\n"
        )
        master.flush()

    print(f"\n{'=' * 70}")
    print("  Benchmark Complete!")
    print(f"  Completed: {completed}/{total}  Failed: {failed}")
    print(f"  Results: {results_path}")
    print(f"  Schedule: {schedule_path}")
    print(f"  Master log: {master_log_path}")
    print(f"{'=' * 70}\n")


if __name__ == "__main__":
    main()
