#!/usr/bin/env python3
"""
FenixAI Benchmark Suite Runner.

Enhanced experiment scheduler that:
  - Reads JSON plan files generated by generate_experiment_plans.py
  - Runs each slot via run_hybrid_live_paper.py (subprocess)
  - Fires auto-analysis after each slot
  - Generates benchmark_results_{tag}.jsonl with per-slot metrics
  - Supports resume from last completed slot
  - Alerts on poor performance (accuracy < 30%, timeout > 40%)

Usage:
    # Full experiment
    python scripts/run_benchmark_suite.py --plan-json plans/exp1_model_matrix.json

    # Quick smoke test (5-min slots, max 2 slots)
    python scripts/run_benchmark_suite.py --plan-json plans/exp1_model_matrix.json \
        --slot-minutes 5 --max-slots 2

    # Resume interrupted run
    python scripts/run_benchmark_suite.py --plan-json plans/exp1_model_matrix.json \
        --resume-from 5
"""

from __future__ import annotations

import argparse
